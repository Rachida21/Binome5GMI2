class Regime {
    private String nom;
    private int prixAuKg;
    
    
    public Regime(String nom, int prixAuKg) {
        this.nom = nom;
        this.prixAuKg = prixAuKg;
    }
    
	public String getNom() {
		return nom;
	}
	
	public int getPrixAuKg() {
		return prixAuKg;
	}
	 
    
    
    
    
}
